// switch statement

 let day = "monday";

switch (day) {
    case "monday": 
        console.log("monday");
        break; 
    case "tuesday": 
        console.log("tuesday");
        break; 
    case "wednesday":
        console.log("wednesday");
        break; 
    default:
        console.log("invalid day");
}

