// Loops in Javascript

// 1. While loop

let n = 0

while(n<=10){
    console.log("number", n)
    n++
}

let i = 0

while(i<5){
    console.log(i)
    i++
}

// print 0 to 100 even number 

let count = 0

while(count <= 100){
    console.log(count)
    count = count + 2
}


// do..while loop

let j = 0

do{
    console.log(j)
    j++
}while(j<=10)